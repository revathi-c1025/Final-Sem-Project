    def _build_run_test(self, steps):
        body_lines = []
        for step in steps:
            sn = step["step_number"]
            desc = step.get("description", "").strip()
            expected = step.get("expected_result", "").strip()
            desc_short = desc[:100].replace('\n', ' ')
            body_lines.append(f"        # --- Step {sn} ---")
            body_lines.append(
                f'        LOGGER.info(f"Step {{self.step_count}}: {self._escape_fstring(desc_short)}")')
            impl = self._generate_step_impl(sn, desc, expected)
            body_lines.extend(impl)
            body_lines.append("")
        return "\n".join(body_lines) + "\n"

