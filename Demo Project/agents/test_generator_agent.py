    @staticmethod
    def _escape_fstring(text):
        return text.replace("{", "{{").replace("}", "}}").replace('"', '\\"')

    def _regenerate_with_template(self, tc, previous_code, error_info):
        traceback_text = error_info.get("traceback", "")
        code = previous_code
        if "ModuleNotFoundError" in traceback_text or "ImportError" in traceback_text:
            m = re.search(r"No module named '(\w+)'", traceback_text)
            if m:
                code = f"import {m.group(1)}\n" + code
        if "SSLError" in traceback_text and "urllib3.disable_warnings" not in code:
            code = "import urllib3\nurllib3.disable_warnings(urllib3.exceptions.InsecureRequestWarning)\n" + code
        if code == previous_code:
            self.log_event("regenerate_fallback", "No specific fix found, regenerating from template")
            repo_ctx = self._repo_ctx.build_generation_context(tc)
            code = self._generate_with_template(tc, repo_ctx)
        return code


