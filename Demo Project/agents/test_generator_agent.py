class TestGeneratorAgent(BaseAgent):
    """
    Agent that generates executable Python test code from test case steps.
    Uses LLM when available, falls back to template-based generation.
    Leverages a reference test repository for context-aware generation.
    """

    def __init__(self):
        super().__init__("TestGeneratorAgent")
        self._llm_available = bool(LLM_API_KEY)
        self._repo_ctx = RepoContext()
        if self._repo_ctx.available:
            n_tests = len(self._repo_ctx.get_test_files())
            n_libs = len(self._repo_ctx.get_library_modules())
            self.log_event("init", f"Reference repo loaded: {n_tests} test files, {n_libs} library modules")
        else:
            self.log_event("init", "No reference repo configured (REFERENCE_REPO_LOCAL_PATH)")
        if self._llm_available:
            self.log_event("init", f"TestGeneratorAgent initialized with LLM: {LLM_PROVIDER}/{LLM_MODEL}")
        else:
            self.log_event("init", "TestGeneratorAgent initialized (template mode - no LLM_API_KEY set)")

    def generate_test(self, test_case_data):
        """Generate an executable Python test file from a parsed test case."""
        self.start_timer()
        pid = test_case_data.get("pid", "unknown")
        self.log_event("generate_start", f"Generating test for {pid}")

        os.makedirs(GENERATED_TESTS_DIR, exist_ok=True)

        repo_ctx = self._repo_ctx.build_generation_context(test_case_data)
        if repo_ctx["repo_available"]:
            n_sim = len(repo_ctx["similar_tests"])
            self.log_event("repo_context", f"Found {n_sim} similar test(s) in reference repo")

        if self._llm_available:
            code = self._generate_with_llm(test_case_data, repo_ctx)
            method = "llm"
        else:
            code = self._generate_with_template(test_case_data, repo_ctx)
            method = "template"

        safe_pid = re.sub(r'[^a-zA-Z0-9_]', '_', pid)
        filename = f"test_{safe_pid}.py"
        file_path = os.path.join(GENERATED_TESTS_DIR, filename)

        with open(file_path, "w", encoding="utf-8") as f:
            f.write(code)

        self.log_event("generate_complete",
                        f"Generated test for {pid} via {method}: {filename}",
                        {"file_path": file_path, "method": method,
                         "lines": code.count('\n') + 1,
                         "repo_context_used": repo_ctx["repo_available"],
                         "elapsed_s": self.elapsed_seconds()})

        return {
            "file_path": file_path,
            "code": code,
            "method": method,
            "test_case_id": pid,
            "repo_context_used": repo_ctx["repo_available"],
        }

    def regenerate_test(self, test_case_data, previous_code, error_info):
        """Regenerate test code after a failure, incorporating error context."""
        self.start_timer()
        pid = test_case_data.get("pid", "unknown")
        self.log_event("regenerate_start", f"Regenerating test for {pid} after failure")

        if self._llm_available:
            code = self._regenerate_with_llm(test_case_data, previous_code, error_info)
            method = "llm_fix"
        else:
            code = self._regenerate_with_template(test_case_data, previous_code, error_info)
            method = "template_fix"

        safe_pid = re.sub(r'[^a-zA-Z0-9_]', '_', pid)
        filename = f"test_{safe_pid}.py"
        file_path = os.path.join(GENERATED_TESTS_DIR, filename)

        with open(file_path, "w", encoding="utf-8") as f:
            f.write(code)

        self.log_event("regenerate_complete",
                        f"Regenerated test for {pid} via {method}: {filename}",
                        {"file_path": file_path, "method": method,
                         "elapsed_s": self.elapsed_seconds()})

        return {"file_path": file_path, "code": code, "method": method, "test_case_id": pid}

    # =========================================================================
    # LLM-based generation
    # =========================================================================

    def _generate_with_llm(self, test_case_data, repo_ctx=None):
        prompt = self._build_generation_prompt(test_case_data, repo_ctx)
        response = self._call_llm(prompt)
        return self._extract_code_from_response(response)

    def _regenerate_with_llm(self, test_case_data, previous_code, error_info):
        prompt = self._build_fix_prompt(test_case_data, previous_code, error_info)
        response = self._call_llm(prompt)
        return self._extract_code_from_response(response)

    def _build_generation_prompt(self, tc, repo_ctx=None):
        steps_text = ""
        for step in tc.get("steps", []):
            steps_text += f"\nStep {step['step_number']}:\n"
            steps_text += f"  Description: {step['description']}\n"
            steps_text += f"  Expected Result: {step['expected_result']}\n"

        ref_context_block = ""
        if repo_ctx and repo_ctx.get("repo_available"):
            ref_context_block += "\n\n===== REFERENCE REPOSITORY CONTEXT =====\n"
            if repo_ctx.get("common_imports"):
                ref_context_block += "\nCOMMON IMPORTS:\n"
                for imp in repo_ctx["common_imports"][:15]:
                    ref_context_block += f"  {imp}\n"
            if repo_ctx.get("library_summary"):
                ref_context_block += f"\nAVAILABLE LIBRARY MODULES:\n{repo_ctx['library_summary'][:2000]}\n"
            if repo_ctx.get("similar_tests"):
                ref_context_block += "\nSIMILAR EXISTING TESTS:\n"
                for sim in repo_ctx["similar_tests"]:
                    ref_context_block += f"\n--- {sim['file']} (similarity: {sim['score']}) ---\n"
                    ref_context_block += sim["code"][:3000] + "\n"

        return f"""You are a test automation engineer for ShopEasy E-Commerce Platform.
Generate a Python test script using the shopease_framework.

TEST CASE: {tc.get('pid', '')} - {tc.get('name', '')}
DESCRIPTION: {tc.get('description', '')}
PRECONDITION: {tc.get('precondition', '')}

TEST STEPS:
{steps_text}
{ref_context_block}

CRITICAL RULES:
1. Use `super(ClassName, self).__init__()` (explicit class name)
2. Call `self.__dict__.update(self.params)` as FIRST line in pre_testcase
3. Call `ShopEaseBaseTestCase.pre_testcase(self, testbed_obj)` in pre_testcase
4. `run_test(self)` takes NO testbed_obj argument
5. Use `self.api_system` for all API calls
6. Use `LOGGER.info(f"Step {{self.step_count}}: ...")` for each step
7. Use `TestUtils.test_assert_equals(expected, actual, message)` for validation

Generate ONLY the Python code, no explanations."""

    def _build_fix_prompt(self, tc, previous_code, error_info):
        return f"""Fix this failed test script.

TEST CASE: {tc.get('pid', '')} - {tc.get('name', '')}

PREVIOUS CODE:
```python
{previous_code}
```

ERROR:
{error_info.get('error_type', 'N/A')}: {error_info.get('error_message', 'N/A')}
Traceback:
{error_info.get('traceback', 'N/A')}

Generate ONLY the fixed Python code."""

    def _call_llm(self, prompt):
        try:
            if LLM_PROVIDER == "openai":
                return self._call_openai(prompt)
            elif LLM_PROVIDER == "azure":
                return self._call_azure(prompt)
            else:
                self.log_event("llm_error", f"Unknown LLM provider: {LLM_PROVIDER}")
                return ""
        except Exception as e:
            self.log_event("llm_error", f"LLM call failed: {e}")
            return ""

    def _call_openai(self, prompt):
        import openai
        client = openai.OpenAI(api_key=LLM_API_KEY, base_url=LLM_BASE_URL or None)
        response = client.chat.completions.create(
            model=LLM_MODEL,
            messages=[
                {"role": "system", "content": "You are an expert test automation engineer."},
                {"role": "user", "content": prompt},
            ],
            temperature=LLM_TEMPERATURE, max_tokens=LLM_MAX_TOKENS,
        )
        return response.choices[0].message.content

    def _call_azure(self, prompt):
        import openai
        client = openai.AzureOpenAI(
            api_key=LLM_API_KEY, azure_endpoint=LLM_BASE_URL, api_version="2024-02-01",
        )
        response = client.chat.completions.create(
            model=LLM_MODEL,
            messages=[
                {"role": "system", "content": "You are an expert test automation engineer."},
                {"role": "user", "content": prompt},
            ],
            temperature=LLM_TEMPERATURE, max_tokens=LLM_MAX_TOKENS,
        )
        return response.choices[0].message.content

    def _extract_code_from_response(self, response):
        if not response:
            return self._generate_with_template({})
        pattern = r'```(?:python)?\s*\n(.*?)```'
        matches = re.findall(pattern, response, re.DOTALL)
        if matches:
            return matches[0].strip()
        return response.strip()

