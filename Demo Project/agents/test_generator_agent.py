    def _generate_with_template(self, tc, repo_ctx=None):
        pid = tc.get("pid", "UNKNOWN")
        safe_pid = re.sub(r'[^a-zA-Z0-9_]', '_', pid)
        safe_pid_lower = safe_pid.lower()
        name = tc.get("name", "Unnamed Test Case")
        name_short = name[:120]
        description = tc.get("description", "N/A")
        precondition = tc.get("precondition", "N/A")
        steps = tc.get("steps", [])

        marker = self._detect_marker(name)
        init_instance_vars = self._build_init_vars(steps)
        run_test_body = self._build_run_test_standalone(steps)
        run_steps_doc = self._build_run_steps_doc(steps)

        # Default test parameters
        product_category = "Electronics"
        test_email = "testuser@example.com"
        warehouse_id = "WH-001"

        return _TEMPLATE.format(
            pid=pid, safe_pid=safe_pid, safe_pid_lower=safe_pid_lower,
            name=name, name_short=name_short,
            description=description[:500], precondition=precondition[:500],
            timestamp=datetime.now().isoformat(), marker=marker,
            init_instance_vars=init_instance_vars,
            run_test_body=run_test_body,
            run_steps_doc=run_steps_doc,
            product_category=product_category,
            test_email=test_email,
            warehouse_id=warehouse_id,
        )

    def _detect_marker(self, name):
        name_lower = name.lower()
        if "comprehensive" in name_lower:
            return "Comprehensive"
        elif "regression" in name_lower:
            return "Regression"
        elif "smoke" in name_lower:
            return "Smoke"
        elif "integration" in name_lower:
            return "Integration"
        return "Comprehensive"

    def _collect_imports(self, steps):
        all_text = " ".join(
            s.get("description", "") + " " + s.get("expected_result", "")
            for s in steps
        ).lower()

        needed_keys = set()
        for keyword, import_keys in _KEYWORD_IMPORT_MAP.items():
            if keyword in all_text:
                needed_keys.update(import_keys)
        needed_keys.add("product")

        lines = []
        seen = set()
        for key in sorted(needed_keys):
            imp = _MODEL_IMPORTS.get(key)
            if imp:
                for line in imp.split("\n"):
                    if line and line not in seen:
                        lines.append(line)
                        seen.add(line)
        return "\n".join(lines)

    def _build_parameters(self, steps):
        all_text = " ".join(
            s.get("description", "") + " " + s.get("expected_result", "")
            for s in steps
        ).lower()
        params = []
        if any(kw in all_text for kw in ["product", "catalog", "sku"]):
            params.append(
                '            Parameter(name="product_category", param_type="string", '
                'default="Electronics",\n'
                '                      help="Default product category for test operations"),')
        if any(kw in all_text for kw in ["order", "checkout", "payment"]):
            params.append(
                '            Parameter(name="customer_id", param_type="string", '
                'default="CUST-001",\n'
                '                      help="Customer ID for order operations"),')
        if any(kw in all_text for kw in ["user", "register", "auth", "login"]):
            params.append(
                '            Parameter(name="test_email", param_type="string", '
                'default="testuser@example.com",\n'
                '                      help="Test user email address"),')
        if any(kw in all_text for kw in ["inventory", "stock", "warehouse"]):
            params.append(
                '            Parameter(name="warehouse_id", param_type="string", '
                'default="WH-001",\n'
                '                      help="Warehouse ID for inventory operations"),')
        if not params:
            params.append('            # No additional parameters required for this test case')
        return "\n".join(params)

